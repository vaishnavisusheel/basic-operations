public class BasicOperation {
    public static void main(String[] args) {
        int a = 15;
        int b = 5;
        int c = 2;
        int sum = a+b+c;
        int diff = a-b-c;
        int prod = a*b*c;
        System.out.println("Addition: "+sum);
        System.out.println("Subtraction: "+diff);
        System.out.println("Multiplication: "+prod);
    }
    
}